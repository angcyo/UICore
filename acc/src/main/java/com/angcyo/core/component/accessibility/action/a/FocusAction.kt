class FocusAction : BaseAction() {

    init {
        handleAction = ConstraintBean.ACTION_FOCUS
    }

    override fun runAction(
        autoParseAction: AutoParseAction,
        service: BaseAccessibilityService,
        constraintBean: ConstraintBean,
        handleNodeList: List<AccessibilityNodeInfoCompat>,
        handleResult: HandleResult
    ): Boolean {
        var value = false
        handleNodeList.forEach {
            value = it.focus() || value
        }
        autoParseAction.handleActionLog("设置焦点:$value")
        return value
    }
}